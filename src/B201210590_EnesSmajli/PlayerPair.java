package B201210590_EnesSmajli;

//This class will be used to get pair from a function
public class PlayerPair {

		//initializing two Colonies that will be part of the pair
	    Colony player1;
	    Colony player2;

	    //Constructor of the class that sets colonies of pair from the parameters
	    public PlayerPair(Colony player1, Colony player2) {
	        // Setting first parameter as first colony
	    	this.player1 = player1;
	    	// Setting second parameter as second colony
	        this.player2 = player2;
	    }

}
